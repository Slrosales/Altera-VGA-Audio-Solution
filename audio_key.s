	.global	PS2_ISR
PS2_ISR:
	subi	sp, sp, 60				# reserve space on the stack
	stw		ra, 0(sp)
	stw		r10, 4(sp)
	stw		r11, 8(sp)
	stw		r12, 12(sp)
	stw		r13, 16(sp)
	stw		r14, 20(sp)
	stw		r15, 24(sp)
