	#srli  r13, r13, 4			# se desplaza 4 bits a la derecha
	#movi  r13, 0x00 # esto no sirve para nada
#	ldbu  r15, REPRESENTACION(r13)	# la representación en 7 segmentos
	#slli  r15, r15, 8			# se desplaza la representación 8 bits a la izquierda
	#or	r14, r14, r15
