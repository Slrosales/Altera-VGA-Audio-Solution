WriteTwoSamples:
	movi r14, 0x1C #A
    beq r12, r14, reA 
	movi r14, 0x24 #E
    beq r12, r14, reE
	movi r14, 0x43 #I
    beq r12, r14, reI
	movi r14, 0x44 #O	
    beq r12, r14, reO
	movi r14, 0x3C #U
    beq r12, r14, reU
	br END_PS2_ISR	

reA:
    ldw  r18, A(r19)
    br WriteSamples

reE:
    ldw  r18, E(r19)
    br WriteSamples

reI:
    ldw  r18, I(r19)
    br WriteSamples

reO:
    ldw  r18, O(r19)
    br WriteSamples

reU:
    ldw  r18, U(r19)
    br WriteSamples


WriteSamples:
    #ldw  r18, 0(r19)
	# se decrementa hasta llegar a 0
    stwio r18, 8(r20)
    stwio r18, 12(r20)
    subi r19, r19, 4
    bge r19, r0, WaitForWriteSpace

HalfPeriodInvertWaveform:
    mov r19, r22
	# r23 es cuanto va durar el sonido
	subi r23, r23, 1
	beq r23,r0, END_PS2_ISR
	movi r12,0x00
    br WaitForWriteSpace


END_PS2_ISR:
	ldw		ra,  0(sp)				# Restore all used register to previous
	ldw		r10, 4(sp)
	ldw		r11, 8(sp)
	ldw		r12, 12(sp)
	ldw		r13, 16(sp)
	ldw		r14, 20(sp)
	ldw		r15, 24(sp)
	ldw		r16, 28(sp)
	ldw		r17, 32(sp)
	ldw		r18, 36(sp)
	ldw		r19, 40(sp)
	ldw		r20, 44(sp)
	ldw		r21, 48(sp)
	ldw		r22, 52(sp)
	ldw		r23, 56(sp)
	
	
	addi	sp,  sp, 60

	ret
