/*******************************************************************************
 * EXCEPTIONS SECTION
 * Note: "ax" is REQUIRED to designate the section as allocatable and executable.
 * Also, the Monitor Program automatically places the ".exceptions" section at 
 * the exception location specified in the CPU settings in SOPC Builder.
 ******************************************************************************/
	.section	.exceptions, "ax"
	.global		EXCEPTION_HANDLER

EXCEPTION_HANDLER:
	subi	sp, sp, 16				# make room on the stack
	stw		et, 0(sp)
/*
La descripción de los registros de control se encuentra en
la página 43 de
Nios II Processor Reference Guide
https://www.intel.com/content/dam/www/programmable/us/en/pdfs/literature/hb/nios2/n2cpu-nii5v1gen2.pdf

 */
	rdctl	et, ctl4 # en el registro de control 4
	# se activa el bit correspondiente a la interrupción configurada
	# cuando esta ocurre
	beq		et, r0, SKIP_EA_DEC		# interrupt is not external
	# el registro ea contiene la dirección de la instrucción
	# que se debe ejecutar cuando termine la interrupción
	# se le restan 4 para que la instrucción
	# que fue interrumpida se ejecute completamente
	# al terminar la interrupción
	subi	ea, ea, 4				# must decrement ea by one instruction
									#  for external interrupts, so that the
									#  interrupted instruction will be run
SKIP_EA_DEC:
	stw		ea, 4(sp)				# save all used registers on the Stack
	stw		ra, 8(sp)				# needed if call inst is used*/
	stw		r22, 12(sp)
	
	rdctl	et, ctl4
	bne		et, r0, CHECK_PS2	# interrupt is an external interrupt*/
# Si llega a este punto es porque
# lo que activo el EXCEPTION_HANDLER no fue
# ninguna de la interrupciones que fueron configuradas
NOT_EI:								# exception must be unimplemented instruction or TRAP
	br		END_ISR					# instruction. This code does not handle those cases

CHECK_PS2: # verificamos si se activó la interrupción del PS/2
	andi	r22, et, 0b10000000
	beq		r22, r0, END_ISR # en caso contrario, termina este proceso
	call	PS2_ISR				

END_ISR:
	ldw		et, 0(sp)				# restore all used register to previous values
	ldw		ea, 4(sp)					
	ldw		ra, 8(sp)				# needed if call inst is used
	ldw		r22, 12(sp)
	addi	sp, sp, 16

	eret
#	.end


