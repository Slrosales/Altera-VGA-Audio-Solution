	stw		r19, 40(sp)
	stw		r20, 44(sp)
	stw		r21, 48(sp)
	stw		r22, 52(sp)
	stw		r23, 56(sp)

	
	movia	r10, PS2_BASE # base address of PS/2 port
	movia	r11, HEX3_HEX0_BASE	# Address of LEDs 
	
	ldwio	r12, (r10)		# Read the state of PS2
	# ahora se debe mostrar el código correspondiente en el display de 7 segmentos
