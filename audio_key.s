	# Aquí va el código de audio
    movia r20, 0xff203040	# Audio device base address: DE1-SoC
    movi r22, 4252				# Half period = 48 samples
    mov r19, r22
	movia r23, 0x2F
WaitForWriteSpace:
    ldwio r16, 4(r20) # indica cuantos datos de 8 bits
    andhi r17, r16, 0xff00
    beq r17, r0, WaitForWriteSpace
    andhi r17, r16, 0xff
    beq r17, r0, WaitForWriteSpace
	
# poner un case
#subi es un if
# subi v1, v2, lo que hace
# beq igual
# bge mayor igual
# la tecla viene de r12
