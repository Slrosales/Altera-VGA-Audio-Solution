/* PROGRAMA PRINCIPAL */
	.text
	.global _start
_start:
	movia 	sp, SDRAM_END - 3	# stack starts from largest memory address
	movia	r3, PS2_BASE # base address of PS/2 port
	/* Se configura la interrupción del teclado PS/2 */
	movi	r7, 0b1	# se debe poner en 1 el bit RE 
					# del segundo registro de configuración (base + 4)
					# para
	stwio	r7, 4(r3) # activar las interrupciones
					  # del puerto PS/2 (base + 4)
	
	/* enable Nios II processor interrupts */
	movia	r7, 0b10000000		# se activa la interrupción
								# para el puerto PS/2 (IRQ 7)
	wrctl	ienable, r7			# se habilitan las interrupciones
	movi	r7, 1
	wrctl	status, r7			# se enciende el procesamioento
								# de interrupciones en el Nios II
	
ESPERAR:
	br		ESPERAR

